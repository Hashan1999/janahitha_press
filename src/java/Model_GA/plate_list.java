package Model_GA;

/**
 *
 * @author Pasindu Maduranga
 */
public class plate_list {
    private String plate_no;
    private String plate_count;
    private String description;
    private String rack_no;
    
    

    /**
     * @return the plate_no
     */
    public String getPlate_no() {
        return plate_no;
    }

    /**
     * @param plate_no the plate_no to set
     */
    public void setPlate_no(String plate_no) {
        this.plate_no = plate_no;
    }

    /**
     * @return the plate_count
     */
    public String getPlate_count() {
        return plate_count;
    }

    /**
     * @param plate_count the plate_count to set
     */
    public void setPlate_count(String plate_count) {
        this.plate_count = plate_count;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the rack_no
     */
    public String getRack_no() {
        return rack_no;
    }

    /**
     * @param rack_no the rack_no to set
     */
    public void setRack_no(String rack_no) {
        this.rack_no = rack_no;
    }
    
    
}
